export const getMoviesByGenre = (genre) => genre === "All" ? movies : movies.filter(m => m.genre === genre);

export const getSimilarMovies = (movie, limit = 6) =>
  movies.filter(m => m.id !== movie.id && (m.genre === movie.genre || m.category === movie.category)).slice(0, limit);

export const getTrending = () => movies.filter(m => m.trending);

export const getTopRated = () => movies.filter(m => m.topRated).sort((a, b) => b.rating - a.rating);

export const getTVShows = () => movies.filter(m => m.category === "tvshows");

export const getMoviesOnly = () => movies.filter(m => m.category === "movies");
